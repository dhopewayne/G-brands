// Typing animation for hero section
document.addEventListener('DOMContentLoaded', function() {
    // Get hero section elements
    const heroSectionEl = document.getElementById('hero');
    const profileImg = document.querySelector('.profile-img');
    const heroH2 = document.querySelector('#hero h2');
    const heroP = document.querySelector('#hero p');
    const heroBtn = document.querySelector('#hero .btn');

    // Populate hero section from data object
    profileImg.src = window.heroSection.profileImage;
    profileImg.alt = window.heroSection.profileAlt;

    // Set hero background image dynamically
    heroSectionEl.style.setProperty('--hero-bg', `url('${window.heroSection.background}')`);

    heroH2.setAttribute('data-text', window.heroSection.h2);
    heroP.setAttribute('data-text', window.heroSection.p);
    heroBtn.setAttribute('data-text', window.heroSection.a);
    heroBtn.href = "#contact"; // Keep the contact link

    // Set initial text content for typing animation
    const h2Text = window.heroSection.h2;
    const pText = window.heroSection.p;
    const btnText = window.heroSection.a;

    // Initialize elements for typing animation
    heroH2.textContent = '';
    heroP.textContent = '';
    heroBtn.textContent = '';
    heroBtn.style.opacity = '0';
    heroBtn.style.transform = 'translateY(20px)';

    let h2Index = 0;
    let pIndex = 0;
    let btnIndex = 0;

    // Type H2
    function typeH2() {
        if (h2Index < h2Text.length) {
            heroH2.textContent += h2Text.charAt(h2Index);
            h2Index++;
            setTimeout(typeH2, 100);
        } else {
            // Stop blinking cursor for H2 and add glow effect
            heroH2.style.borderRight = 'none';
            heroH2.style.animation = 'textGlow 2s ease-in-out infinite';
            // Start typing P after a short delay
            setTimeout(typeP, 500);
        }
    }

    // Type P
    function typeP() {
        if (pIndex < pText.length) {
            heroP.textContent += pText.charAt(pIndex);
            pIndex++;
            setTimeout(typeP, 80);
        } else {
            // Stop blinking cursor for P and add glow effect
            heroP.style.borderRight = 'none';
            heroP.style.animation = 'textGlow 2s ease-in-out infinite';
            // Show button after a short delay
            setTimeout(showButton, 300);
        }
    }

    // Show and type button
    function showButton() {
        heroBtn.style.opacity = '1';
        heroBtn.style.transform = 'translateY(0)';
        heroBtn.style.transition = 'all 0.5s ease';

        function typeBtn() {
            if (btnIndex < btnText.length) {
                heroBtn.textContent += btnText.charAt(btnIndex);
                btnIndex++;
                setTimeout(typeBtn, 60);
            } else {
                // Stop blinking cursor for button and add glow effect
                heroBtn.style.borderRight = 'none';
                heroBtn.style.animation = 'textGlow 2s ease-in-out infinite';
            }
        }

        setTimeout(typeBtn, 200);
    }

    // Start the animation after the profile image animation
    setTimeout(typeH2, 1200);

    // Render About Section
    renderAboutSection();

    // Render Skills Section
    renderSkillsSection();

    // Render Projects Section
    renderProjectsSection();

    // Render Experience Section
    renderExperienceSection();

    // Render Testimonials Section
    renderTestimonialsSection();

    // Render Contact Section
    renderContactSection();
});

