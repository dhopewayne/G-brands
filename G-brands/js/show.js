// Testimonials Slider Functionality
function initializeTestimonialsSlider() {
    let currentTestimonial = 0;
    const testimonialCards = document.querySelectorAll('.testimonial-card');
    const indicators = document.querySelectorAll('.indicator');
    let testimonialInterval;

    function showTestimonial(index) {
        // Hide all testimonials
        testimonialCards.forEach(card => {
            card.classList.remove('active');
        });
        indicators.forEach(indicator => {
            indicator.classList.remove('active');
        });

        // Show the current testimonial
        testimonialCards[index].classList.add('active');
        indicators[index].classList.add('active');
        currentTestimonial = index;
    }

    function nextTestimonial() {
        currentTestimonial = (currentTestimonial + 1) % testimonialCards.length;
        showTestimonial(currentTestimonial);
    }

    function startTestimonialSlider() {
        testimonialInterval = setInterval(nextTestimonial, 4000); // Change slide every 4 seconds
    }

    function stopTestimonialSlider() {
        clearInterval(testimonialInterval);
    }

    // Event listeners for indicators
    indicators.forEach((indicator, index) => {
        indicator.addEventListener('click', () => {
            showTestimonial(index);
            stopTestimonialSlider();
            startTestimonialSlider(); // Restart auto-play
        });
    });

    // Pause auto-play on hover
    const testimonialsSlider = document.querySelector('.testimonials-slider');
    if (testimonialsSlider) {
        testimonialsSlider.addEventListener('mouseenter', stopTestimonialSlider);
        testimonialsSlider.addEventListener('mouseleave', startTestimonialSlider);
    }

    // Initialize slider
    if (testimonialCards.length > 0) {
        showTestimonial(0);
        startTestimonialSlider();
    }
}

